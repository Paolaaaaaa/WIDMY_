from ..models import Usuario


def create_usuario(usuario):
    usuario_nuevo = Usuario(
        id_usuario= usuario["id_usuario"]
        
    )
    usuario_nuevo.save()
    return usuario_nuevo



def get_usuario(pk_):
    usuario = Usuario.objects.get(pk=pk_)
    return usuario
